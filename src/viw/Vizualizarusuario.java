     private void carregarusuariovizualizar(){
        listausuario = controlerusuarios.Listarusuarioscontrolervizualizar();
        DefaultTableModel modelo = (DefaultTableModel) tabelausuariovizualizar.getModel();
        modelo.setNumRows(0);
        for (int i = 0; i < listausuario.size(); i++) {
            modelo.addRow(new Object[]{
                listausuario.get(i).getCodigo(),
                listausuario.get(i).getUsuario(),
                listausuario.get(i).getTelefone(),
                listausuario.get(i).getCpf(),
                listausuario.get(i).getNomeusuario(),
                listausuario.get(i).getEmail()
            });
        }
    }
