    private void metododeletarusuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_metododeletarusuarioActionPerformed
        int linha = tabelausuariovizualizar.getSelectedRow();
        if (linha < 0) {
            JOptionPane.showMessageDialog(this, "Nenhum registro selecionado!");
            return;
        }
        int codigo=(int) tabelausuariovizualizar.getValueAt(linha,0);
           int entrada = JOptionPane.showConfirmDialog(null, "Deseja excluir","", JOptionPane.YES_NO_OPTION);
           if(entrada==JOptionPane.YES_OPTION){
               controlerusuarios.excluirusuario(codigo);
               carregarusuariovizualizar();
               JOptionPane.showMessageDialog(null,"Apagado");
           }
    }//GEN-LAST:event_metododeletarusuarioActionPerformed

    private void pesquisarusuarioKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_pesquisarusuarioKeyReleased
        DefaultTableModel model = (DefaultTableModel) tabelausuariovizualizar.getModel();
        TableRowSorter<DefaultTableModel> trs = new TableRowSorter<>(model);
        tabelausuariovizualizar.setRowSorter(trs);
        trs.setRowFilter(RowFilter.regexFilter(pesquisarusuario.getText()));
    }//GEN-LAST:event_pesquisarusuarioKeyReleased

