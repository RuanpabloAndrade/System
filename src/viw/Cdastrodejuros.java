    private void metodosalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_metodosalvarActionPerformed
       recebuveus = new Modelrecebiveis();
       recebuveus.setJuros(Double.parseDouble(juros.getText()));
       recebuveus.setJurosdiasmensal(Integer.parseInt(diasmensal.getText()));
       if (controlersalvarjuros.Salvarjuros(recebuveus)) {
            JOptionPane.showMessageDialog(null, "Juros cadastrado com Sucesso!");
            CarregarJuros();
            Limpar();
        } else {
            JOptionPane.showMessageDialog(null, "Juros n√£o cadastrado!");
            CarregarJuros();
        }
       
    }//GEN-LAST:event_metodosalvarActionPerformed
public void CarregarJuros(){
        listaJuros = controlersalvarjuros.ListarJuros();
        DefaultTableModel modelo=(DefaultTableModel) tabelajuros.getModel();
        modelo.setNumRows(0);
        for (int i = 0; i <listaJuros.size(); i++) {
            modelo.addRow(new Object[]{
                listaJuros.get(i).getJuros(),
                listaJuros.get(i).getJurosdiasmensal(),
                listaJuros.get(i).getDiario()
            });
        }
    }
    
